import { afterEach, beforeEach, describe, expect, it, vi } from "vitest";
import type { Env } from "../env";
import type { Comic } from "../types/comic";
import type { ProcessLogEntry } from "../types/processLog";
import { app } from "../index";

interface ComicDocument extends Comic {
  user_id: string;
}
interface LogDocument extends ProcessLogEntry {
  user_id: string;
  ts: string;
}

// Fake Astra Data API, keyed by collection name so `comics` and `process_log`
// stay separate. Only the network boundary is mocked — the stores, the tool
// executors and runAgent all run for real against these fakes.
let comicDocs: ComicDocument[] = [];
let logDocs: LogDocument[] = [];

vi.mock("@datastax/astra-db-ts", () => {
  class FakeCollection {
    constructor(private readonly name: string) {}

    private get docs(): Array<Record<string, unknown>> {
      const docs = this.name === "process_log" ? logDocs : comicDocs;
      return docs as unknown as Array<Record<string, unknown>>;
    }

    find(filter: { user_id: string }) {
      const results = this.docs.filter((d) => d.user_id === filter.user_id);
      return { toArray: async () => results };
    }

    async findOne(filter: { user_id: string; comic_id: string }) {
      return this.docs.find(
        (d) => d.user_id === filter.user_id && d.comic_id === filter.comic_id,
      );
    }

    async insertOne(doc: Record<string, unknown>) {
      this.docs.push(doc);
      return { insertedId: doc.comic_id };
    }

    async findOneAndUpdate(
      filter: { user_id: string; comic_id: string },
      update: { $set: Record<string, unknown> },
    ) {
      const doc = this.docs.find(
        (d) => d.user_id === filter.user_id && d.comic_id === filter.comic_id,
      );
      if (!doc) return undefined;
      Object.assign(doc, update.$set);
      return doc;
    }

    async deleteOne() {
      return { deletedCount: 0 };
    }
  }

  class FakeDb {
    collection(name: string) {
      return new FakeCollection(name);
    }
  }

  class DataAPIClient {
    db() {
      return new FakeDb();
    }
  }

  return { DataAPIClient };
});

const fakeTokens = new Map([["test-token", "demo-user"]]);

